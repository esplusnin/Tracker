import UIKit

extension UIColor {
    static let backgroundDay = UIColor(named: "backgroundDay") ?? UIColor.systemGray
    static let backgroundNight = UIColor(named: "backgroundNight") ?? UIColor.systemGray2
    static let blackDay = UIColor(named: "blackDay") ?? UIColor.black
    static let blackNight = UIColor(named: "blackNight") ?? UIColor.white
    static let blue = UIColor(named: "blue") ?? UIColor.systemBlue
    static let red = UIColor(named: "red") ?? UIColor.systemRed
    static let whiteDay = UIColor(named: "whiteDay") ?? UIColor.white
    static let whiteNight = UIColor(named: "whiteNight") ?? UIColor.black
}
